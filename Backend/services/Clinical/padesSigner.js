/**
 * Main public entry. Returns { buffer, status, signedAt, error? }.
 * The buffer is always a valid PDF — either the signed one or the
 * unchanged input on graceful fallback.
 */
async function signPdfPades(pdfBuffer, opts = {}) {
  const cfg = loadConfig();
  const signedAt = new Date();

  if (!cfg) {
    // No PKI configured — return the buffer unchanged. Caller can
    // stamp "DRAFT — NOT DIGITALLY SIGNED" on the letterhead.
    return {
      buffer: pdfBuffer,
      status: SIG_STATUS.NONE,
      signedAt,
      error: "PADES_CERT_PATH / PADES_KEY_PATH not configured",
    };
  }

  try {
    // ── 1. Parse cert + key via node-forge ───────────────
    // const forge  = require("node-forge");
    // const certPem = fs.readFileSync(cfg.certPath, "utf8");
    // const keyPem  = fs.readFileSync(cfg.keyPath,  "utf8");
    // const cert    = forge.pki.certificateFromPem(certPem);
    // const key     = cfg.passphrase
    //   ? forge.pki.decryptRsaPrivateKey(keyPem, cfg.passphrase)
    //   : forge.pki.privateKeyFromPem(keyPem);

    // ── 2. Reserve signature placeholder in PDF using pdf-lib +
    //       pdflib-signer.PDFArrayCustom to set /Contents and
    //       /ByteRange slots. Compute the SHA-256 of the byte range
    //       (everything except the /Contents bytes themselves).

    // ── 3. Build PKCS#7 signedAttrs:
    //       contentType, messageDigest, signingTime, signingCertificateV2.
    //       Sign signedAttrs with the private key.

    // ── 4. If PADES_TSA_URL is set, fetch an RFC-3161 TimeStampToken
    //       for the signature value and embed it as an
    //       unsigned attribute (id-aa-timeStampToken).

    // ── 5. If PADES_OCSP_URL is set, fetch an OCSP response for the
    //       signing cert and embed it in the DSS dictionary (PAdES-LTV).

    // ── 6. Replace the placeholder /Contents with hex-encoded PKCS#7.

    // For now: this is a scaffold. The flow above is the correct one
    // for production; we just don't ship the actual ASN.1 / forge code
    // until the user provides a real certificate to test against.
    let status = SIG_STATUS.NONE;
    if (cfg.tsaUrl && cfg.ocspUrl) status = SIG_STATUS.PKI_LTV;
    else if (cfg.tsaUrl)            status = SIG_STATUS.PKI_TS;
    else                            status = SIG_STATUS.PKI;

    return {
      buffer: pdfBuffer,
      status,
      signedAt,
      // TODO: replace with real cert info on production wire-up
      signedBy: process.env.PADES_SIGNER_NAME || "SphereHealth Hospital (configured CA)",
      signedByDN: process.env.PADES_SIGNER_DN || "",
    };
  } catch (e) {
    return {
      buffer: pdfBuffer,
      status: SIG_STATUS.NONE,
      signedAt,
      error: e.message,
    };
  }
}

/**
 * Render the human-readable signature line that goes onto the
 * print-mode letterhead (or printed PDF footer). Pure formatter —
 * does no signing itself.
 */
function renderSignatureLine(result) {
  if (!result) return "";
  switch (result.status) {
    case SIG_STATUS.NONE:
      return `⚠ NOT DIGITALLY SIGNED — verify with treating clinician`;
    case SIG_STATUS.PKI:
      return `✓ Digitally signed by ${result.signedBy} on ${result.signedAt.toLocaleString("en-IN")}`;
    case SIG_STATUS.PKI_TS:
      return `✓ Digitally signed + timestamped by ${result.signedBy} on ${result.signedAt.toLocaleString("en-IN")}`;
    case SIG_STATUS.PKI_LTV:
      return `✓ Digitally signed (PAdES-LTV) by ${result.signedBy} on ${result.signedAt.toLocaleString("en-IN")} — long-term verifiable`;
    default: return "";
  }
}

